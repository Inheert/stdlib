#include "libft.h"

void	*ft_memset(void *ptr, int value, size_t size)
{
	unsigned char	*_ptr;

	_ptr = (unsigned char *)ptr;
	while (size-- > 0)
	{
		*_ptr++ = value;
	}
	return (ptr);
}
